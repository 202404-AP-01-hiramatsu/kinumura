ALTER TABLE slip
ADD COLUMN IF NOT EXISTS email_address VARCHAR(100);

ALTER TABLE slip_media
ADD COLUMN IF NOT EXISTS project_name VARCHAR(100);

ALTER TABLE slip_detail
ADD COLUMN IF NOT EXISTS returned BOOLEAN DEFAULT FALSE;

ALTER TABLE slip_detail
ADD COLUMN IF NOT EXISTS returned_date DATE;

CREATE TABLE IF NOT EXISTS master_setting (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    master_text VARCHAR(1000),
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

